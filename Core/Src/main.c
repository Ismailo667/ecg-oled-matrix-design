float applyHighPass(float x_in) {
    float w_n = x_in - highPassCoeffs[4] * w_hp[0] - highPassCoeffs[5] * w_hp[1];
    float y_n = highPassCoeffs[0] * w_n + highPassCoeffs[1] * w_hp[0] + highPassCoeffs[2] * w_hp[1];
    w_hp[1] = w_hp[0];
    w_hp[0] = w_n;
    return y_n;
}

float applyBiquadSOS(float x_in) {
    float x_out = x_in;
    for (int s = 0; s < NUM_SECTIONS; s++) {
        float w_n = x_out - sosCoeffs[s][4] * w[s][0] - sosCoeffs[s][5] * w[s][1];
        x_out = sosCoeffs[s][0] * w_n + sosCoeffs[s][1] * w[s][0] + sosCoeffs[s][2] * w[s][1];
        w[s][1] = w[s][0];
        w[s][0] = w_n;
    }
    return x_out;
}

float movingAverageFilter(float newSample) {
    bufferSum -= buffer[bufferIndex];
    buffer[bufferIndex] = newSample;
    bufferSum += newSample;
    bufferIndex = (bufferIndex + 1) % WINDOW_SIZE;
    return bufferSum / WINDOW_SIZE;
}

float detectPeakAndComputeBPM(float valFiltre) {
    uint32_t now = HAL_GetTick();
    maxAmplitude = fmaxf(maxAmplitude, valFiltre);
    threshold = 0.7f * maxAmplitude;  // Seuil adaptatif
    if (valFiltre > threshold && !peakDetected && (now - lastPeakTime) > REFRACTORY_MS) {
        if (lastPeakTime != 0) {
            BPM = 60000.0f / (float)(now - lastPeakTime);
        }
        lastPeakTime = now;
        peakDetected = 1;
        return 1.0f;
    }
    if (valFiltre < (threshold * 0.5f)) peakDetected = 0;
    return 0.0f;
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {
    uint32_t rawVal = HAL_ADC_GetValue(hadc);
    float rawVoltage = (ADC_REF_VOLTAGE * rawVal) / ADC_MAX_VALUE;
    //float rawVoltage=rawVal*1.0f;
    float highPassed = applyHighPass(rawVoltage);
    float biquadFiltered = applyBiquadSOS(highPassed);
    float filtered = movingAverageFilter(biquadFiltered);
    float peakFlag = detectPeakAndComputeBPM(filtered);
    char msg[64];
    sprintf(msg, "%.4f,%.4f,%.1f\r\n", rawVoltage, filtered, BPM);
    HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
}


